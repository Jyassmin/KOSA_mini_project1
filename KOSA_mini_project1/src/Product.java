
public class Product {
	int id;
	String name;
	String brand;
	int size;
	String color;
	int stock;
	long cost;

	
	Product (int id,
			String name,
			String brand,
			int size,
			String color,
			int stock,
			long cost) {
		
		this.id = id;
		this.name = name;
		this.brand = brand;
		this.size = size;
		this.color = color;
		this.stock = stock;
		this.cost = cost;
	}
	
}
